#include <cstdlib>
#include <fstream>
#include <string.h>
#include <sys/sysinfo.h>
#include <unistd.h>

#include <android-base/properties.h>
#define _REALLY_INCLUDE_SYS__SYSTEM_PROPERTIES_H_
#include <sys/_system_properties.h>

#include "vendor_init.h"
#include "property_service.h"

using android::base::GetProperty;

char const *heapstartsize;
char const *heapgrowthlimit;
char const *heapsize;
char const *heapminfree;
char const *heapmaxfree;
char const *heaptargetutilization;

char const  *build_fp = "google/redfin/redfin:11/RQ2A.210505.003/7255357:user/release-keys";
char const  *build_desc = "redfin-user 11 RQ2A.210505.003 7255357 release-keys";

int property_set(const char *key, const char *value) {
    return __system_property_set(key, value);
}

void property_override(char const prop[], char const value[])
{
    prop_info *pi;

    pi = (prop_info*) __system_property_find(prop);
    if (pi)
        __system_property_update(pi, value, strlen(value));
    else
        __system_property_add(prop, strlen(prop), value, strlen(value));
}

void property_override_dual(char const system_prop[], char const vendor_prop[], char const value[])
{
    property_override(system_prop, value);
    property_override(vendor_prop, value);
}

void set_device_ugglite()
{
    property_override_dual("ro.product.device", "ro.product.vendor.device", "ugglite");
    property_override_dual("ro.product.model", "ro.product.vendor.model", "Redmi Note 5A Lite");
    property_set("ro.xiaomi.device", "ugglite");
    property_set("ro.xiaomi.series", "ulysse");
}

void set_device_ugg()
{
    property_override_dual("ro.product.device", "ro.product.vendor.device", "ugg");
    property_override_dual("ro.product.model", "ro.product.vendor.model", "Redmi Note 5A Prime");
    property_set("ro.xiaomi.device", "ugg");
    property_set("ro.xiaomi.series", "ulysse");
}

void check_device()
{
    struct sysinfo sys;

    sysinfo(&sys);

    if (sys.totalram > 5072ull * 1024 * 1024) {
        // from - phone-xhdpi-6144-dalvik-heap.mk
        heapstartsize = "16m";
        heapgrowthlimit = "256m";
        heapsize = "512m";
        heaptargetutilization = "0.5";
        heapminfree = "8m";
        heapmaxfree = "32m";
    } else if (sys.totalram > 3072ull * 1024 * 1024) {
        // from - phone-xxhdpi-4096-dalvik-heap.mk
        heapstartsize = "8m";
        heapgrowthlimit = "256m";
        heapsize = "512m";
        heaptargetutilization = "0.6";
        heapminfree = "8m";
        heapmaxfree = "16m";

        // set device name to ugg
        set_device_ugg();
    } else if (sys.totalram > 2048ull * 1024 * 1024) {
        // from - phone-xhdpi-2048-dalvik-heap.mk
        heapstartsize = "8m";
        heapgrowthlimit = "192m";
        heapsize = "512m";
        heaptargetutilization = "0.75";
        heapminfree = "512k";
        heapmaxfree = "8m";

        // set device name to ugg
        set_device_ugg();
    } else {
        // from - phone-xhdpi-2048-dalvik-heap.mk
        heapstartsize = "8m";
        heapgrowthlimit = "192m";
        heapsize = "512m";
        heaptargetutilization = "0.75";
        heapminfree = "512k";
        heapmaxfree = "8m";

        // set device name to ugglite
        set_device_ugglite();
    }
}

void vendor_load_properties()
{
    check_device();

    property_set("dalvik.vm.heapstartsize", heapstartsize);
    property_set("dalvik.vm.heapgrowthlimit", heapgrowthlimit);
    property_set("dalvik.vm.heapsize", heapsize);
    property_set("dalvik.vm.heaptargetutilization", heaptargetutilization);
    property_set("dalvik.vm.heapminfree", heapminfree);
    property_set("dalvik.vm.heapmaxfree", heapmaxfree);

    // Pixel identification
    property_override("ro.build.description", build_desc);
    property_override("ro.build.fingerprint", build_fp);
    property_override("ro.system.build.fingerprint", build_fp);

    // Front Flashlight
    property_set("persist.s5k3p8sp.flash.low","320");
    property_set("persist.s5k3p8sp.flash.light","300");
    property_set("persist.ov16885.flash.low","290");
    property_set("persist.ov16885.flash.light","275");
}
